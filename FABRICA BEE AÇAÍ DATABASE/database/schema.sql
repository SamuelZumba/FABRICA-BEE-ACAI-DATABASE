  NUMERO VARCHAR(20) NULL,
  COMPLEMENTO VARCHAR(80) NULL,
  BAIRRO VARCHAR(100) NULL,
  CODIGO_MUNICIPIO VARCHAR(7) NULL,
  MUNICIPIO VARCHAR(100) NULL,
