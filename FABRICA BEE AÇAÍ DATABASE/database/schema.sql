USE fabrica_bee_acai;

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;


-- CADASTROS E ACESSO

CREATE TABLE usuario (
  ID_USUARIO INT UNSIGNED AUTO_INCREMENT,
  NOME VARCHAR(120) NOT NULL,
  CPF VARCHAR(14) NOT NULL,
  EMAIL VARCHAR(120) NOT NULL,
  FOTO_PERFIL VARCHAR(255) NULL,
  CARGO VARCHAR(80) NULL,
  SENHA VARCHAR(255) NOT NULL,
  NIVEL_ACESSO VARCHAR(20) NOT NULL DEFAULT 'Colaborador',
  STATUS_USUARIO VARCHAR(20) NOT NULL DEFAULT 'Ativo',
  TOKEN_RECUP VARCHAR(255) NULL,
  DATA_EXP_TOKEN DATETIME NULL,
  TEMA VARCHAR(10) NOT NULL DEFAULT 'light',
  DATA_CADASTRO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,

  CONSTRAINT PK_USUARIO PRIMARY KEY (ID_USUARIO),
  CONSTRAINT UK_USUARIO_CPF UNIQUE (CPF),
  CONSTRAINT UK_USUARIO_EMAIL UNIQUE (EMAIL),
  CONSTRAINT CK_USUARIO_NIVEL
    CHECK (NIVEL_ACESSO IN ('Admin', 'Colaborador')),
  CONSTRAINT CK_USUARIO_STATUS
    CHECK (STATUS_USUARIO IN ('Ativo', 'Desativado')),
  CONSTRAINT CK_USUARIO_TEMA
    CHECK (TEMA IN ('light', 'dark'))
) ENGINE=InnoDB;



