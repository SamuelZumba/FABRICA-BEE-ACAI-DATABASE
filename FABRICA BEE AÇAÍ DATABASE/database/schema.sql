CREATE TABLE embalagem (
  ID_EMBALAGEM INT UNSIGNED AUTO_INCREMENT,
  NOME VARCHAR(100) NOT NULL,
  CAPACIDADE DECIMAL(12,3) NOT NULL,
  UNIDADE_CAPACIDADE VARCHAR(10) NOT NULL,
  UNIDADE_MEDIDA VARCHAR(10) NOT NULL DEFAULT 'UN',
  QTD_ESTOQUE DECIMAL(12,3) NOT NULL DEFAULT 0,
  QTD_RESERVADA DECIMAL(12,3) NOT NULL DEFAULT 0,
  ESTOQUE_MINIMO DECIMAL(12,3) NOT NULL DEFAULT 10,

  CONSTRAINT PK_EMBALAGEM PRIMARY KEY (ID_EMBALAGEM),
  CONSTRAINT UK_EMBALAGEM_CAPACIDADE
    UNIQUE (NOME, CAPACIDADE, UNIDADE_CAPACIDADE),
  CONSTRAINT CK_EMBALAGEM_CAPACIDADE CHECK (CAPACIDADE > 0),
  CONSTRAINT CK_EMBALAGEM_UNIDADE_CAP
    CHECK (UNIDADE_CAPACIDADE IN ('UN', 'KG', 'G', 'L', 'ML')),
  CONSTRAINT CK_EMBALAGEM_UNIDADE_MEDIDA
    CHECK (UNIDADE_MEDIDA = 'UN'),
  CONSTRAINT CK_EMBALAGEM_QTD CHECK (QTD_ESTOQUE >= 0),
  CONSTRAINT CK_EMBALAGEM_RESERVADA
    CHECK (QTD_RESERVADA >= 0 AND QTD_RESERVADA <= QTD_ESTOQUE),
  CONSTRAINT CK_EMBALAGEM_MINIMO CHECK (ESTOQUE_MINIMO >= 0)
) ENGINE=InnoDB;




-- PRODUCAO, PRODUTOS ACABADOS E DESCARTES

CREATE TABLE producao (
  ID_PRODUCAO INT UNSIGNED AUTO_INCREMENT,
  NOME_PRODUTO VARCHAR(120) NOT NULL,
  TAMANHO DECIMAL(12,3) NOT NULL,
  UNIDADE_MEDIDA VARCHAR(10) NOT NULL,
  LOTE VARCHAR(50) NOT NULL,
  QTD_NO_LOTE DECIMAL(12,3) NOT NULL,
  ETAPA_ATUAL VARCHAR(30) NOT NULL DEFAULT 'Preparacao',
  TEMPO_RESFRIAMENTO_HORAS INT NULL,
  INICIO_RESFRIAMENTO DATETIME NULL,
  FIM_RESFRIAMENTO DATETIME NULL,
  NOTIFICADO_RESFRIAMENTO TINYINT(1) NOT NULL DEFAULT 0,
  DATA_FABRICACAO DATE NOT NULL,
  DATA_VALIDADE DATE NULL,
  ID_USUARIO INT UNSIGNED NOT NULL,
  ID_EMBALAGEM INT UNSIGNED NULL,
  QTD_EMBALAGEM_RESERVADA DECIMAL(12,3) NOT NULL DEFAULT 0,
  EMBALAGEM_BAIXADA TINYINT(1) NOT NULL DEFAULT 0,

  CONSTRAINT PK_PRODUCAO PRIMARY KEY (ID_PRODUCAO),
  CONSTRAINT UK_PRODUCAO_LOTE UNIQUE (LOTE),
  CONSTRAINT CK_PRODUCAO_TAMANHO CHECK (TAMANHO > 0),
  CONSTRAINT CK_PRODUCAO_QTD CHECK (QTD_NO_LOTE > 0),
  CONSTRAINT CK_PRODUCAO_UNIDADE
    CHECK (UNIDADE_MEDIDA IN ('UN', 'KG', 'G', 'L', 'ML')),
  CONSTRAINT CK_PRODUCAO_ETAPA
    CHECK (ETAPA_ATUAL IN (
      'Preparacao', 'Batimento', 'Embalagem',
      'Resfriamento', 'Liberado', 'Descartado'
    )),
  CONSTRAINT CK_PRODUCAO_TEMPO
    CHECK (
      TEMPO_RESFRIAMENTO_HORAS IS NULL
      OR TEMPO_RESFRIAMENTO_HORAS > 0
    ),
  CONSTRAINT CK_PRODUCAO_NOTIFICADO
    CHECK (NOTIFICADO_RESFRIAMENTO IN (0, 1)),
  CONSTRAINT CK_PRODUCAO_EMBALAGEM_RESERVADA
    CHECK (QTD_EMBALAGEM_RESERVADA >= 0),
  CONSTRAINT CK_PRODUCAO_EMBALAGEM_BAIXADA
    CHECK (EMBALAGEM_BAIXADA IN (0, 1)),
  CONSTRAINT FK_PRODUCAO_USUARIO
    FOREIGN KEY (ID_USUARIO)
    REFERENCES usuario (ID_USUARIO)
    ON UPDATE CASCADE
    ON DELETE RESTRICT,
  CONSTRAINT FK_PRODUCAO_EMBALAGEM
    FOREIGN KEY (ID_EMBALAGEM)
    REFERENCES embalagem (ID_EMBALAGEM)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
) ENGINE=InnoDB;


